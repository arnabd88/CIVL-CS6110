package edu.udel.cis.vsl.civl.model.IF.expression;

/**
 * The initializer of the i-th range of a domain.
 * 
 * @author Manchun Zheng
 * 
 */
public interface DomainInitializer extends Expression {

	/**
	 * The index of the range to be initialized.
	 * 
	 * @return The index of the range to be initialized.
	 */
	int index();

	/**
	 * The domain to be initialized.
	 * 
	 * @return The domain to be initialized.
	 */
	Expression domain();

	/**
	 * The number of ranges that the domain contains.
	 * 
	 * @return The number of ranges that the domain contains.
	 */
	int dimension();
}
