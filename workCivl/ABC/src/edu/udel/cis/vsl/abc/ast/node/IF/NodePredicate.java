package edu.udel.cis.vsl.abc.ast.node.IF;

/**
 * A predicate on AST nodes, i.e., a function which given an {@link ASTNode},
 * returns either <code>true</code> or <code>false</code>.
 * 
 * @author siegel
 * 
 */
public interface NodePredicate {

	/**
	 * Applies this predicate to the given node.
	 * 
	 * @param node
	 *            any AST node
	 * @return <code>true</code> if this predicate hold at the node,
	 *         <code>false</code> otherwise
	 */
	boolean holds(ASTNode node);

}
