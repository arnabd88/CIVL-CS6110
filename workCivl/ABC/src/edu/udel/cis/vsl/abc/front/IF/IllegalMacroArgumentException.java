package edu.udel.cis.vsl.abc.front.IF;

import org.antlr.runtime.Token;


public class IllegalMacroArgumentException extends PreprocessorRuntimeException {
	/**
	 * Eclipse made me do it.
	 */
	private static final long serialVersionUID = -5902334316575784297L;

	public IllegalMacroArgumentException(Token token) {
		super(
				"Are you sure you want to use that preprocessor directive in a macro argument?",
				token);
	}
}
